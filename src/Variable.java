public class Variable {
    public static void main(String[] args) {
        int myNum  = 865; //initialization
        int YourNum; //declaration
        System.out.println(myNum);
        YourNum = 856;
        System.out.println(YourNum);
        YourNum = 15; // overwritten
        System.out.println(YourNum);

        float myFloat = 5.0f; //'f' is to tell that that is num will get fit in variable
        System.out.println(myFloat);

        double myDouble = 6.98452;
        System.out.println(myDouble);

        String wish = "good morning";
        System.out.println(wish);

        boolean isVeg = true;
        System.out.println(isVeg);
    }
}
